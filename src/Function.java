public class Function {
    private int a;
    private int b;
    private int c;

    public Function (int aK, int bK, int cK) {
        a = aK;
        b = bK;
        c = cK;
    }

    public int getA() {
        return a;
    }

    public int getB() {
        return b;
    }

    public int getC() {
        return c;
    }
}
